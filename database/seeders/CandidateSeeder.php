                </ul>
                
                <h3>&nbsp;</h3>
                
                <h3><strong>Գրավոր քննություն.</strong>&nbsp;</h3>
                
                <p>&nbsp;</p>
                
                <p>Գրավոր քննությունը կազմակերպում է ԲԴԽ-ն: Քննությունն անցկացվում է համապատասխանաբար քրեական, քաղաքացիական կամ վարչական մասնագիտացման ոլորտում գրավոր առաջադրանքների միջոցով, որոնք, բացի տեսական իրավական գիտելիքների ստուգումից, ուղղված են նաև հավակնորդի վերլուծական և իրավակիրառ կարողությունների ստուգմանը:</p>
                
