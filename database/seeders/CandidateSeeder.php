                <p>Յուրաքանչյուր մասնագիտացման բաժնի համար քվեարկելիս հավակնորդների թիվն անհրաժեշտ քանակին հավասար կամ շատ լինելու դեպքում Բարձրագույն դատական խորհրդի անդամի ունեցած ձայների թիվը հավասար է համալրման համար անհրաժեշտ քանակին, իսկ քիչ լինելու դեպքում՝ հավակնորդների թվին: Եթե քվեարկության մասնակիցն ավելի ձայն է տվել, քան սահմանված է սույն մասով, ապա այդ քվեաթերթիկը համարվում է անվավեր:</p>
                
