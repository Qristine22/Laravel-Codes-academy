                    <li><strong>4.</strong> Հայերենին տիրապետելու հանգամանքը հավաստվում է ուսումնական հաստատություններում հայերենով կրթություն ստացած կամ կրթական ծրագրերով նախատեսված՝ հայոց լեզու առարկայի ուսումնառությունն ավարտած և ամփոփիչ ատեստավորում անցած լինելու վերաբերյալ ուսումնական հաստատությունների տված ավարտական փաստաթղթով (վկայական, ատեստատ, դիպլոմ): Հայերենին տիրապետելու հանգամանքը հավաստող ավարտական փաստաթղթի բացակայության դեպքում հայերենին տիրապետելու հանգամանքն ստուգվում է կրթության և գիտության ոլորտում Կառավարության քաղաքականությունը մշակող և իրականացնող նախարարի սահմանած կարգով, որը պետք է նախատեսի հայերենին տիրապետելու հանգամանքի ստուգման ողջամիտ, օբյեկտիվ չափորոշիչներ, ինչպես նաև գործընթացի վերահսկողության ընթացակարգեր:</li>
                    <li><strong>5.</strong> ՀՀ դատական օրենսգիրք սահմանադրական օրենքի իմաստով՝ մասնագիտական աշխատանքի փորձառությունը բակալավրի կամ դիպլոմավորված մասնագետի բարձրագույն իրավաբանական կրթության որակավորման աստիճան ձեռք բերելուց հետո իրավունքի ոլորտում իրականացված մասնագիտական գործունեությունն է, եթե այլ բան սահմանված չէ սույն օրենսգրքով:</li>
